export function normalizeAppLanguage(
  value: string | null | undefined
): AppLanguage {
  const normalized = String(value ?? "").toLowerCase();
  const primaryLanguage = normalized.split("-")[0];

  const exact = supportedAppLanguages.find(
    (lang) => lang.toLowerCase() === normalized
  );

  if (exact) {
    return exact;
  }

  const primary = supportedAppLanguages.find(
    (lang) => lang.toLowerCase() === primaryLanguage
  );

  if (primary) {
    return primary;
  }

  return "en";
}

export function createTranslator(
  language: AppLanguage
): (key: MessageKey) => string {
  return (key: MessageKey) => {
    return messages[language]?.[key] ?? messages.en[key] ?? key;
  };
}
